@Composable
fun BookListScreenRoot(
    viewModel: BookListViewModel = koinViewModel(),
    onBookClicked: (book: Book) -> Unit,
    modifier: Modifier = Modifier
) {
    val state by viewModel.state.collectAsStateWithLifecycle()
    BookListScreen(
        state = state,
        onAction = { action ->
            when (action) {
                is BookListAction.OnBookClicked -> onBookClicked(action.book)
                else -> Unit
            }
            viewModel.onAction(action)
        },
        modifier = modifier
    )
}

@Composable
fun BookListScreen(
    state: BookListState,
    onAction: (BookListAction) -> Unit,
    modifier: Modifier = Modifier
) {
    val pagerState = rememberPagerState { 2 }
    val searchedResultLazyListState = rememberLazyListState()
    val favouritesLazyListState = rememberLazyListState()
    val keyboardController = LocalSoftwareKeyboardController.current
    LaunchedEffect(state.searchedResults){
        searchedResultLazyListState.animateScrollToItem(0)
    }
    LaunchedEffect(state.selectedTabIndex){
        pagerState.animateScrollToPage(state.selectedTabIndex)
    }

    LaunchedEffect(pagerState.currentPage){
        onAction(BookListAction.OnTabSelected(pagerState.currentPage))
    }
    Column(
        modifier = modifier.fillMaxSize().background(DarkBlue).statusBarsPadding(),
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        BookSearchBar(
            searchedQuery = state.searchedQuery,
            onSearchedQueryChanged = { searched ->
                onAction(BookListAction.OnSearchedQueryChanged(searched))
            },
            onImeAction = {
                keyboardController?.hide()
            },
            modifier = Modifier.widthIn(max = 400.dp).fillMaxWidth().padding(16.dp)
        )
        Surface(
            modifier = Modifier.weight(1f).fillMaxWidth(),
            color = DesertWhite,
            shape = RoundedCornerShape(
                topStart = 32.dp,
                topEnd = 32.dp
            )
        ) {
            Column(
                horizontalAlignment = Alignment.CenterHorizontally
            ) {
                TabRow(
                    selectedTabIndex = state.selectedTabIndex,
                    modifier = Modifier.padding(vertical = 12.dp)
                        .widthIn(max = 700.dp)
                        .fillMaxWidth(),
                    indicator = { tabPositions ->
                        TabRowDefaults.SecondaryIndicator(
                            color = SandYellow,
                            modifier = Modifier.tabIndicatorOffset(tabPositions[state.selectedTabIndex])
                        )
                    }
                ) {
                    Tab(
                        selected = state.selectedTabIndex == 0,
                        onClick = {
                            onAction(BookListAction.OnTabSelected(0))
                        },
                        modifier = Modifier.weight(1f),
                        selectedContentColor = SandYellow,
                        unselectedContentColor = Color.Black.copy(
                            alpha = 0.5f
                        )
                    ) {
                        Text(
                            text = stringResource(Res.string.search_results),
                            modifier = Modifier.padding(vertical = 12.dp),
                            style = MaterialTheme.typography.titleMedium
                        )
                    }
                    Tab(
                        selected = state.selectedTabIndex == 1,
                        onClick = {
                            onAction(BookListAction.OnTabSelected(1))
                        },
                        modifier = Modifier.weight(1f),
                        selectedContentColor = SandYellow,
                        unselectedContentColor = Color.Black.copy(
                            alpha = 0.5f
                        )
                    ) {
                        Text(
                            text = stringResource(Res.string.favourites),
                            modifier = Modifier.padding(vertical = 12.dp),
                            style = MaterialTheme.typography.titleMedium
                        )
                    }
                }
                HorizontalPager(
                    state = pagerState,
                    modifier = Modifier.fillMaxWidth().weight(1f)
                ) { pageIndex ->
                    Box(
                        modifier = Modifier.fillMaxSize(),
                        contentAlignment = Alignment.Center
                    ) {
                        when (pageIndex) {
                            0 -> {
                                if (state.isLoading) PulseAnimation(modifier = Modifier.size(60.dp))
                                else {
                                    when {
                                        state.errorMessage != null -> {
                                            Text(text = state.errorMessage.asString(),
                                                textAlign = TextAlign.Center,
                                                style = MaterialTheme.typography.headlineSmall,
                                                color = MaterialTheme.colorScheme.error)
                                        }
                                        state.searchedResults.isEmpty() -> {
                                            Text(text = stringResource(Res.string.no_search_results),
                                                textAlign = TextAlign.Center,
                                                style = MaterialTheme.typography.headlineSmall,
                                                color = MaterialTheme.colorScheme.error)
                                        }
                                        else -> {
                                            BookList(
                                                books = state.searchedResults,
                                                onBookClick = { book ->
                                                    onAction(BookListAction.OnBookClicked(book))
                                                },
                                                modifier = Modifier.fillMaxSize(),
                                                scrollState = searchedResultLazyListState
                                            )
                                        }
                                    }
                                }
                            }
                            1->{
                                if(state.favouriteBooks.isEmpty()){
                                    Text(text = stringResource(Res.string.no_favourite_results),
                                        textAlign = TextAlign.Center,
                                        style = MaterialTheme.typography.headlineSmall,
                                        color = MaterialTheme.colorScheme.error)
                                }
                                else{
                                    BookList(
                                        books = state.favouriteBooks,
                                        onBookClick = { book ->
                                            onAction(BookListAction.OnBookClicked(book))
                                        },
                                        modifier = Modifier.fillMaxSize(),
                                        scrollState = searchedResultLazyListState
                                    )
                                }
                            }
                        }
                    }
                }
            }
        }
    }
}